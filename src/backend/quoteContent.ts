/**
 * Get random quote from specified category
 * @param category Quote category
 * @returns Random quote string in Hindi
 */
export function getRandomQuote(category: QuoteCategory): string {
  const quotes = quotesByCategory[category];
  if (!quotes || quotes.length === 0) {
    return "जीवन सुंदर है, इसे जिओ।"; // fallback quote
  }
  return quotes[Math.floor(Math.random() * quotes.length)];
}

/**
 * Get all quotes for a category
 * @param category Quote category
 * @returns Array of quotes
 */
export function getQuotesByCategory(category: QuoteCategory): string[] {
  return quotesByCategory[category] || [];
}
